void Game_Flag::Draw(void)
{
	if (m_alpha < 225) {
		m_alpha++;
	}
	Sprite_DrawAlpha(m_fade_tex, 0, 0, SCREEN_WIDTH, SCREEN_HEIGHT, m_alpha);

	if (m_is_gameover) {
		Sprite_DrawAlpha(m_death_tex, 400, 100, 400, 400, m_alpha);
	}
	else {
		Sprite_DrawAlpha(m_clear_tex, 230, 100, 800, 400, m_alpha);
	}

}

