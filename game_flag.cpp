Game_Flag::Game_Flag(bool is_gameover)
{

	StopSound(SOUND_LABEL_BGM_GAME);
	StopSound(SOUND_LABEL_BGM_BOSS);
	if (is_gameover) {
		PlaySound(SOUND_LABEL_SE_GAMEOVER);
	}
	else {
		PlaySound(SOUND_LABEL_SE_OK);
	}
	m_is_gameover = is_gameover;
	m_death_tex = Texture_SetLoadFile("Asset/Texture/death.png", 400, 400);
	m_clear_tex = Texture_SetLoadFile("Asset/Texture/extermination.png", 800, 400);
	m_fade_tex = Texture_SetLoadFile("Asset/Texture/deathback.png", 16, 16);
	Texture_Load();
	is_nextscene = false;
	is_used = true;

	m_alpha = 0;
}

